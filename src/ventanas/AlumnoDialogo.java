    public ModalResult mostrarDialogo(ArrayList <Alumno> alumno){
        this.alumno = alumno;
        num = 0;
        modalResult = ModalResult.CANCELAR;
        edtMatricula.setEditable(true);
        edtNombre.setText("");
        edtPaterno.setText("");
        edtMaterno.setText("");
        edtMatricula.setText("");
        edtCalificacion.setText("");
        cmbCarrera.setSelectedIndex(-1);
        this.setVisible(true);
        return modalResult;
    }
    
    public ModalResult mostrarAlumno(Alumno modificar){
        num = 1;
        modalResult = ModalResult.CANCELAR;
        edtNombre.requestFocus();
        edtNombre.setText(modificar.getNombre());
        edtPaterno.setText(modificar.getPaterno());
        edtMaterno.setText(modificar.getMaterno());
        edtMatricula.setText(modificar.getMatricula());
        edtMatricula.setEditable(false);
        edtCalificacion.setText(modificar.getCalificacion().toString());
        cmbCarrera.setSelectedIndex(numeroCarrera(modificar.getCarrera()));
        this.setVisible(true);
        return modalResult;
    }
    
    private Integer numeroCarrera(Carrera carrera){
        int numero = -1;
        if(Carrera.ELECTRONICA == carrera){
            numero = 0;
        }
        if(Carrera.ARQUITECTURA == carrera){
            numero = 1;
        }
        if(Carrera.DERECHO == carrera){
            numero = 2;
        }
        if(Carrera.FISIOTERAPIA == carrera){
            numero = 3;
        }
        if(Carrera.MEDICINA == carrera){
            numero = 4;
        }
        
        return numero;
    }
    
