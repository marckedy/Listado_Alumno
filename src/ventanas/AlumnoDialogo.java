    private double validarCalificacion() throws CalificacionException{
        double cal = -1;
        try{
            cal = Double.valueOf(edtCalificacion.getText());
            if ((cal < 0.0) || (cal > 10.0)){
                throw new CalificacionException();
            }
            return cal;
        }catch(Exception e){
            throw new CalificacionException("Hubo un erro en la calificacion");
        }
    }
    
    private String validarNombre() throws NombreException{
        if ("".equals(edtNombre.getText())){
            throw new NombreException("No hay nada en nombre");
            
        }
        return "Si";
    }
    
    private String validarPaterno() throws PaternoException{
        if ("".equals(edtPaterno.getText())){
            throw new PaternoException("No hay nada en el apellido paterno");
        }
        return "Si";
    }
    
    private String validarMaterno() throws MaternoException{
        if ("".equals(edtMaterno.getText())){
            throw new MaternoException("No hay nada en el apellido materna");
        }
        return "Si";
    }
    
    private Integer validarCarrera() throws CarreraException{
        int num = cmbCarrera.getSelectedIndex();
        if(num < 0){
            throw new CarreraException("No ha seleccionada una carrera");
        }
        return num;
    }
    
    private String validarMatricula() throws MatriculaException{
        int n = edtMatricula.getText().length();
        
        if ("".equals(edtMatricula.getText())){
            throw new MatriculaException("No hay nada en la matrícula");
        }
        
        if (n < 9){
            throw new MatriculaException("La matrícula debe incluir 9 caracteres");
        }
        
        if (n > 9){
            throw new MatriculaException("La matrícula solo debe incluir 9 caracteres");
        }
        
        for (int i = 0; i < alumno.size(); i++){
            if (alumno.get(i).getMatricula().equals(edtMatricula.getText())){
                throw new MatriculaException("Esta matrícula ya existe");
            }
        }
        return "Si";
    }
    
    
